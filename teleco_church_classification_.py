# Correct target extraction
y = df['Churn']   # or your actual target column

# Encode binary target
from sklearn.preprocessing import LabelEncoder
le = LabelEncoder()
y = le.fit_transform(y)

from sklearn.model_selection import train_test_split

X = df.drop(columns=['Churn'])

X_train, X_test, y_train, y_test = train_test_split(
    X, y,
    test_size=0.3,
    random_state=42,
    stratify=y   # VERY IMPORTANT
)

from sklearn.pipeline import Pipeline
from sklearn.compose import ColumnTransformer
from sklearn.impute import SimpleImputer
from sklearn.preprocessing import StandardScaler, OneHotEncoder

num_cols = X.select_dtypes(include=['int64', 'float64']).columns
cat_cols = X.select_dtypes(include=['object']).columns

num_pipeline = Pipeline([
    ('imputer', SimpleImputer(strategy='median')),
    ('scaler', StandardScaler())
])

cat_pipeline = Pipeline([
    ('imputer', SimpleImputer(strategy='most_frequent')),
    ('onehot', OneHotEncoder(handle_unknown='ignore'))
])

preprocessor = ColumnTransformer([
    ('num', num_pipeline, num_cols),
    ('cat', cat_pipeline, cat_cols)
])

# Model Building
from sklearn.linear_model import LogisticRegression
from sklearn.ensemble import RandomForestClassifier
from xgboost import XGBClassifier

from sklearn.model_selection import GridSearchCV, StratifiedKFold
cv = StratifiedKFold(n_splits=3, shuffle=True, random_state=42)

models = {
    "logistic": {
        "model": LogisticRegression(max_iter=1000),
        "params": {
            "model__C": [0.1, 1, 10]
        }
    },
    "rf": {
        "model": RandomForestClassifier(),
        "params": {
            "model__n_estimators": [100, 200],
            "model__max_depth": [3, 5, None]
        }
    },
    "xgb": {
        "model": XGBClassifier(use_label_encoder=False, eval_metric='logloss'),
        "params": {
            "model__n_estimators": [100, 200],
            "model__max_depth": [3, 5],
            "model__learning_rate": [0.01, 0.1]
        }
    }
}

best_models = {}

for name, config in models.items():
    print(f"\nTraining {name}...")

    pipe = Pipeline([
        ('preprocessor', preprocessor),
        ('model', config["model"])
    ])

    grid = GridSearchCV(
        pipe,
        config["params"],
        cv=cv,
        scoring='roc_auc',
        n_jobs=-1,
        error_score='raise'   # helps debugging
    )

    grid.fit(X_train, y_train)

    best_models[name] = grid.best_estimator_

    print("Best Params:", grid.best_params_)
    print("Best Score:", grid.best_score_)

from sklearn.metrics import classification_report, roc_auc_score

for name, model in best_models.items():
    y_pred = model.predict(X_test)
    y_proba = model.predict_proba(X_test)[:, 1]

    print(f"\nModel: {name}")
    print(classification_report(y_test, y_pred))
    print("ROC-AUC:", roc_auc_score(y_test, y_proba))

import joblib

# Assuming xgb was the best model
best_model_pipeline = best_models['xgb']

# Save the best model using joblib
joblib.dump(best_model_pipeline, 'churn_model.joblib')
print("Model saved as 'churn_model.joblib' using joblib.")

